def main():
    user_num1 = input("Enter first number: ")
    user_num2 = input("Enter second number: ")
    user_num1 = int(user_num1)
    user_num2 = int(user_num2)
    result = user_num1 + user_num2
    print(f"Sum of {user_num1} and {user_num2} is {result}")



if __name__ == '__main__':
    main()